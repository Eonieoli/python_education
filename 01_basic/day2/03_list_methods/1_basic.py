# 1_basic.py

"""
Day 2 - 3교시: 리스트 수정 (메서드)
Basic (강사 시연용)

학습 목표:
- 리스트 가변성 이해
- append()로 요소 추가
- insert()로 특정 위치에 추가
- 인덱스로 요소 수정
- del, remove(), pop()으로 요소 삭제
"""

# ============================================
# 1. 리스트 가변성 (Mutable)
# ============================================

# 리스트는 생성 후에도 내용을 수정할 수 있습니다 (가변성)
# 문자열은 수정 불가능 (불변성)이지만, 리스트는 수정 가능!

fruits = ["사과", "바나나", "딸기"]
print("원본:", fruits)  # 출력: 원본: ['사과', '바나나', '딸기']

# 리스트 내용을 직접 수정 가능
fruits[1] = "포도"
print("수정 후:", fruits)  # 출력: 수정 후: ['사과', '포도', '딸기']


# ============================================
# 2. append() - 끝에 요소 추가
# ============================================

# append()는 리스트 끝에 새 요소를 추가합니다
shopping = ["우유", "빵"]
print("장바구니:", shopping)  # 출력: 장바구니: ['우유', '빵']

shopping.append("계란")
print("추가 후:", shopping)  # 출력: 추가 후: ['우유', '빵', '계란']

shopping.append("버터")
print("또 추가:", shopping)  # 출력: 또 추가: ['우유', '빵', '계란', '버터']


# ============================================
# 3. insert() - 특정 위치에 추가
# ============================================

# insert(인덱스, 값)는 특정 위치에 요소를 삽입합니다
colors = ["빨강", "파랑", "노랑"]
print("원본:", colors)  # 출력: 원본: ['빨강', '파랑', '노랑']

# 인덱스 1번 위치에 "초록" 삽입
colors.insert(1, "초록")
print("삽입 후:", colors)  # 출력: 삽입 후: ['빨강', '초록', '파랑', '노랑']

# 맨 앞에 삽입 (인덱스 0)
colors.insert(0, "보라")
print("맨 앞 삽입:", colors)  # 출력: 맨 앞 삽입: ['보라', '빨강', '초록', '파랑', '노랑']


# ============================================
# 4. 인덱스로 요소 수정
# ============================================

# 인덱스를 사용해 특정 위치의 값을 변경할 수 있습니다
scores = [85, 90, 75, 88]
print("원본 점수:", scores)  # 출력: 원본 점수: [85, 90, 75, 88]

# 두 번째 점수(인덱스 1) 수정
scores[1] = 95
print("수정 후:", scores)  # 출력: 수정 후: [85, 95, 75, 88]

# 마지막 점수 수정
scores[-1] = 92
print("마지막 수정:", scores)  # 출력: 마지막 수정: [85, 95, 75, 92]


# ============================================
# 5. del - 인덱스로 삭제
# ============================================

# del을 사용하면 특정 인덱스의 요소를 삭제합니다
numbers = [10, 20, 30, 40, 50]
print("원본:", numbers)  # 출력: 원본: [10, 20, 30, 40, 50]

# 인덱스 2번 요소 삭제
del numbers[2]
print("삭제 후:", numbers)  # 출력: 삭제 후: [10, 20, 40, 50]


# ============================================
# 6. remove() - 값으로 삭제
# ============================================

# remove(값)는 리스트에서 특정 값을 찾아 삭제합니다
# 같은 값이 여러 개 있으면 첫 번째 것만 삭제됩니다
animals = ["고양이", "강아지", "토끼", "강아지"]
print("원본:", animals)  # 출력: 원본: ['고양이', '강아지', '토끼', '강아지']

animals.remove("강아지")
print("삭제 후:", animals)  # 출력: 삭제 후: ['고양이', '토끼', '강아지']


# ============================================
# 7. pop() - 제거하고 반환
# ============================================

# pop()은 요소를 삭제하면서 그 값을 반환합니다
# 인자 없으면 마지막 요소, 인덱스 주면 해당 위치 요소 삭제

tasks = ["청소", "빨래", "설거지", "장보기"]
print("할 일:", tasks)  # 출력: 할 일: ['청소', '빨래', '설거지', '장보기']

# 마지막 할 일 꺼내기
last_task = tasks.pop()
print(f"완료: {last_task}")  # 출력: 완료: 장보기
print("남은 일:", tasks)  # 출력: 남은 일: ['청소', '빨래', '설거지']

# 첫 번째 할 일 꺼내기
first_task = tasks.pop(0)
print(f"완료: {first_task}")  # 출력: 완료: 청소
print("남은 일:", tasks)  # 출력: 남은 일: ['빨래', '설거지']


# ============================================
# 8. 실전 예제: 재고 관리 시스템
# ============================================

# 상품 재고 리스트
inventory = ["노트북", "마우스", "키보드"]
print("초기 재고:", inventory)  # 출력: 초기 재고: ['노트북', '마우스', '키보드']

# 신규 상품 입고
inventory.append("모니터")
inventory.append("헤드셋")
print("입고 후:", inventory)  # 출력: 입고 후: ['노트북', '마우스', '키보드', '모니터', '헤드셋']

# 긴급으로 맨 앞에 추가
inventory.insert(0, "웹캠")
print("긴급 입고:", inventory)  # 출력: 긴급 입고: ['웹캠', '노트북', '마우스', '키보드', '모니터', '헤드셋']

# 판매 완료 (pop으로 제거하며 기록)
sold_item = inventory.pop(1)
print(f"판매 완료: {sold_item}")  # 출력: 판매 완료: 노트북
print("현재 재고:", inventory)  # 출력: 현재 재고: ['웹캠', '마우스', '키보드', '모니터', '헤드셋']

# 단종 상품 제거
inventory.remove("웹캠")
print("최종 재고:", inventory)  # 출력: 최종 재고: ['마우스', '키보드', '모니터', '헤드셋']
