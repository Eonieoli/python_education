"""
Day 2 - 6교시: 딕셔너리 기초
Exercise - 사용자 프로필 만들기 (정답)
"""

# ============================================
# 1. 사용자 기본 정보 딕셔너리 생성
# ============================================

user = {
    "username": "coding_master",
    "age": 25,
    "city": "서울"
}


# ============================================
# 2. 추가 정보 업데이트
# ============================================

# bio 키에 자기소개 추가
user["bio"] = "Python을 사랑하는 개발자입니다"

# followers 키에 팔로워 수 추가
user["followers"] = 1234


# ============================================
# 3. 프로필 정보 출력
# ============================================

print("=== 사용자 프로필 ===")
print(f"사용자명: {user['username']}")
print(f"나이: {user['age']}세")
print(f"도시: {user['city']}")
print(f"자기소개: {user['bio']}")
print(f"팔로워: {user['followers']}명")


# ============================================
# 추가 도전 (선택 사항)
# ============================================

# 팔로워 100명 증가
user["followers"] = user["followers"] + 100
print(f"\n팔로워 증가 후: {user['followers']}명")
