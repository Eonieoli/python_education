# 1_basic.py

"""
Day 2 - 5교시: 튜플 + 집합
Basic (강사 시연용)

학습 목표:
- 튜플 생성과 인덱싱
- 튜플 언패킹 (핵심!)
- 여러 변수 동시 할당
- 스와핑 (값 교환)
- 집합 생성과 중복 제거
- in 연산자로 빠른 검색
"""

# ============================================
# 1. 튜플 기본 - 생성과 인덱싱
# ============================================

# 튜플은 괄호()로 생성합니다 (수정 불가능!)
point = (10, 20)
print(point)  # 출력: (10, 20)
print(type(point))  # 출력: <class 'tuple'>

# 인덱싱은 리스트와 동일합니다
print(point[0])  # 출력: 10
print(point[1])  # 출력: 20

# 여러 타입을 섞을 수 있습니다
user = ("홍길동", 25, "서울")
print(user)  # 출력: ('홍길동', 25, '서울')


# ============================================
# 2. 튜플 언패킹 (핵심!) ⭐⭐⭐
# ============================================

# 튜플의 값을 여러 변수에 한 번에 할당할 수 있습니다
coordinates = (100, 200)
x, y = coordinates  # 언패킹!
print(f"x={x}, y={y}")  # 출력: x=100, y=200

# 괄호 없이도 가능합니다
name, age, city = "김철수", 30, "부산"
print(f"{name}님은 {age}살이고 {city}에 삽니다")
# 출력: 김철수님은 30살이고 부산에 삽니다

# RGB 색상값 언패킹
color = (255, 128, 0)  # 주황색
r, g, b = color
print(f"R={r}, G={g}, B={b}")  # 출력: R=255, G=128, B=0


# ============================================
# 3. 스와핑 (값 교환) - 언패킹 활용
# ============================================

# 파이썬에서는 언패킹으로 값을 쉽게 교환할 수 있습니다!
a = 10
b = 20
print(f"교환 전: a={a}, b={b}")  # 출력: 교환 전: a=10, b=20

# 스와핑 (한 줄로!)
a, b = b, a
print(f"교환 후: a={a}, b={b}")  # 출력: 교환 후: a=20, b=10


# ============================================
# 4. 튜플의 불변성 (수정 불가!)
# ============================================

# 튜플은 생성 후 수정할 수 없습니다
position = (50, 100)
# position[0] = 999  # 에러! 튜플은 수정 불가

# 새로운 튜플을 만들어야 합니다
position = (999, 100)  # 가능! (새로운 튜플 생성)
print(position)  # 출력: (999, 100)


# ============================================
# 5. 집합 기본 - 생성과 중복 제거
# ============================================

# 집합은 set()으로 생성합니다 (중복 자동 제거!)
tags = set(["파이썬", "코딩", "파이썬", "개발", "코딩"])
print(tags)  # 출력: {'파이썬', '코딩', '개발'} (순서 무작위)

# 리스트에서 중복 제거할 때 유용합니다
numbers = [1, 2, 2, 3, 3, 3, 4, 4, 4, 4]
unique_numbers = set(numbers)
print(unique_numbers)  # 출력: {1, 2, 3, 4}


# ============================================
# 6. in 연산자로 빠른 검색
# ============================================

# 집합은 in 연산자로 매우 빠르게 검색할 수 있습니다!
allowed_users = {"admin", "user1", "user2"}

# 사용자 확인
if "admin" in allowed_users:
    print("관리자입니다")  # 출력: 관리자입니다

if "hacker" in allowed_users:
    print("허가된 사용자입니다")
else:
    print("접근 거부!")  # 출력: 접근 거부!


# ============================================
# 7. 집합 메서드 - add(), remove()
# ============================================

# 빈 집합 생성
categories = set()

# add()로 요소 추가
categories.add("IT")
categories.add("과학")
categories.add("IT")  # 중복은 무시됨
print(categories)  # 출력: {'IT', '과학'}

# remove()로 요소 제거
categories.remove("과학")
print(categories)  # 출력: {'IT'}


# ============================================
# 8. 실전 예제: 중복 태그 제거 시스템
# ============================================

# 블로그 게시글 태그 (중복 있음)
post_tags = ["파이썬", "웹개발", "FastAPI", "파이썬", "API", "웹개발"]

# 중복 제거
unique_tags = set(post_tags)
print(f"원본: {post_tags}")
print(f"중복 제거: {unique_tags}")
# 출력: 원본: ['파이썬', '웹개발', 'FastAPI', '파이썬', 'API', '웹개발']
# 출력: 중복 제거: {'파이썬', '웹개발', 'FastAPI', 'API'}

# 태그 개수
print(f"원본 태그 개수: {len(post_tags)}")  # 출력: 원본 태그 개수: 6
print(f"고유 태그 개수: {len(unique_tags)}")  # 출력: 고유 태그 개수: 4
