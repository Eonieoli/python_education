"""
Day 2 - 1, 2교시 통합: 리스트 기본 + len()
Practice (강사와 함께 실습)

학습 목표:
- 학생 점수 관리
- 슬라이싱으로 일부 추출
- len()으로 개수 확인
- 리스트 연산 활용

시간: 약 20분 (2교시에서 진행)
방식: 처음부터 끝까지 함께 타이핑
"""

# ============================================
# 실습 1: 학생 점수 관리
# ============================================

# 5명 학생의 점수를 리스트로 저장
scores = [85, 92, 78, 95, 88]

# 전체 점수 출력
print("전체 점수:", scores)
# 출력: 전체 점수: [85, 92, 78, 95, 88]

# 첫 번째 학생 점수
first_score = scores[0]
print(f"첫 번째 학생 점수: {first_score}점")
# 출력: 첫 번째 학생 점수: 85점

# 마지막 학생 점수
last_score = scores[-1]
print(f"마지막 학생 점수: {last_score}점")
# 출력: 마지막 학생 점수: 88점

# 학생 수 확인
student_count = len(scores)
print(f"학생 수: {student_count}명")
# 출력: 학생 수: 5명


# ============================================
# 실습 2: 슬라이싱으로 일부 추출
# ============================================

# 7일간의 기온 데이터
temperatures = [15, 18, 20, 22, 19, 17, 16]

# 처음 3일간의 기온
first_three_days = temperatures[:3]
print("처음 3일:", first_three_days)
# 출력: 처음 3일: [15, 18, 20]

# 마지막 3일간의 기온
last_three_days = temperatures[-3:]
print("마지막 3일:", last_three_days)
# 출력: 마지막 3일: [19, 17, 16]

# 중간 3일 (인덱스 2, 3, 4)
middle_three_days = temperatures[2:5]
print("중간 3일:", middle_three_days)
# 출력: 중간 3일: [20, 22, 19]


# ============================================
# 실습 3: 리스트 연산 활용
# ============================================

# 상반기 매출 (1월~6월)
first_half = [100, 120, 110, 130, 125, 140]

# 하반기 매출 (7월~12월)
second_half = [150, 145, 160, 155, 170, 180]

# 연간 전체 매출 (리스트 합치기)
yearly_sales = first_half + second_half
print("연간 매출:", yearly_sales)
# 출력: 연간 매출: [100, 120, 110, 130, 125, 140, 150, 145, 160, 155, 170, 180]

# 총 매출 개월 수
total_months = len(yearly_sales)
print(f"총 {total_months}개월 데이터")
# 출력: 총 12개월 데이터

# 구분선 만들기 (리스트 반복)
separator = ["-"] * 20
print(separator)
# 출력: ['-', '-', '-', '-', '-', '-', '-', '-', '-', '-', '-', '-', '-', '-', '-', '-', '-', '-', '-', '-']


# ============================================
# 실습 4: 역순과 간격 슬라이싱
# ============================================

# 일주일 동안의 방문자 수
visitors = [120, 150, 180, 200, 190, 250, 300]

# 역순으로 출력 (최근부터)
reversed_visitors = visitors[::-1]
print("역순 방문자:", reversed_visitors)
# 출력: 역순 방문자: [300, 250, 190, 200, 180, 150, 120]

# 격일 데이터 (0, 2, 4, 6번 인덱스)
every_other_day = visitors[::2]
print("격일 방문자:", every_other_day)
# 출력: 격일 방문자: [120, 180, 190, 300]

# 주말 데이터 (마지막 2개)
weekend = visitors[-2:]
print("주말 방문자:", weekend)
# 출력: 주말 방문자: [250, 300]
