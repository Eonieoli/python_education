# 점수 리스트
scores = [85, 92, 78, 95, 88]

# TODO: for문으로 scores를 순회하며 합계를 계산하세요
# 힌트 1: total 변수를 0으로 초기화
# 힌트 2: for score in scores:
# 힌트 3: total += score

print("\n=== 점수 합계 계산 ===")
total = 0
