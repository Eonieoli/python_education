"""
Day 3 - 4교시: for문 기본
Exercise - 짝수 출력과 구구단 (정답)
"""

# ============================================
# Exercise 1: 1부터 20까지 중 짝수만 출력
# ============================================

print("=== 1부터 20까지 짝수 ===")

# 방법 1: range(시작, 끝, 간격) 사용
for i in range(2, 21, 2):  # 2부터 20까지 2씩
    print(i)

# 출력:
# 2
# 4
# 6
# ...
# 20


# ============================================
# Exercise 2: 7단 구구단 만들기
# ============================================

print("\n=== 7단 구구단 ===")

# 1부터 9까지 반복
for i in range(1, 10):
    result = 7 * i
    print(f"7 × {i} = {result}")

# 출력:
# 7 × 1 = 7
# 7 × 2 = 14
# 7 × 3 = 21
# ...
# 7 × 9 = 63


# ============================================
# Exercise 3 (도전): 리스트의 평균 계산
# ============================================

temperatures = [12, 15, 13, 17, 14, 16, 15]

print("\n=== 주간 평균 기온 ===")

# 1. 합계 계산
total = 0
for temp in temperatures:
    total += temp

# 2. 평균 계산
average = total / len(temperatures)

# 3. 결과 출력
print(f"주간 평균 기온: {average:.1f}도")  # 출력: 주간 평균 기온: 14.6도
