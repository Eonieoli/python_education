# ============================================
# 1. for문 기본 문법
# ============================================

# for문은 리스트나 범위의 각 요소를 하나씩 꺼내서 반복합니다
# 문법: for 변수 in 리스트/범위:

# 1부터 5까지 출력
print("=== 1부터 5까지 출력 ===")
for i in range(1, 6):  # 1, 2, 3, 4, 5
    print(i)
# 출력:
# 1
# 2
# 3
# 4
# 5


# ============================================
# 2. range() 함수 - 1개 인자
# ============================================

# range(n): 0부터 n-1까지
print("\n=== range(5) ===")
for i in range(5):  # 0, 1, 2, 3, 4
    print(i)
# 출력:
# 0
# 1
# 2
# 3
# 4


# ============================================
# 3. range() 함수 - 2개 인자
# ============================================

# range(시작, 끝): 시작부터 끝-1까지
print("\n=== range(1, 6) ===")
for i in range(1, 6):  # 1, 2, 3, 4, 5
    print(i)

print("\n=== range(10, 15) ===")
for i in range(10, 15):  # 10, 11, 12, 13, 14
    print(i)


# ============================================
# 4. range() 함수 - 3개 인자
# ============================================

# range(시작, 끝, 간격): 시작부터 끝-1까지 간격만큼
print("\n=== range(0, 10, 2) - 짝수 ===")
for i in range(0, 10, 2):  # 0, 2, 4, 6, 8
    print(i)

print("\n=== range(1, 10, 2) - 홀수 ===")
for i in range(1, 10, 2):  # 1, 3, 5, 7, 9
    print(i)

print("\n=== range(10, 0, -1) - 거꾸로 ===")
for i in range(10, 0, -1):  # 10, 9, 8, ..., 1
    print(i)


# ============================================
# 5. 리스트 순회
# ============================================

# 리스트의 각 요소를 하나씩 꺼내서 사용
fruits = ["사과", "바나나", "딸기", "포도"]

print("\n=== 과일 리스트 순회 ===")
for fruit in fruits:
    print(f"좋아하는 과일: {fruit}")
# 출력:
# 좋아하는 과일: 사과
# 좋아하는 과일: 바나나
# 좋아하는 과일: 딸기
# 좋아하는 과일: 포도


# ============================================
# 6. 실전 예제 1: 학생 점수 출력
# ============================================

# 학생 이름과 점수 리스트
students = ["김철수", "이영희", "박민수", "정수진"]
scores = [85, 92, 78, 95]

print("\n=== 학생 성적표 ===")
for i in range(len(students)):  # range(4) → 0, 1, 2, 3
    print(f"{students[i]}: {scores[i]}점")
# 출력:
# 김철수: 85점
# 이영희: 92점
# 박민수: 78점
# 정수진: 95점


# ============================================
# 7. 실전 예제 2: 상품 목록 처리
# ============================================

# 상품명 리스트
products = ["노트북", "마우스", "키보드", "모니터"]

print("\n=== 상품 목록 ===")
for i in range(len(products)):
    # 상품 번호는 1부터 시작 (i+1)
    print(f"{i+1}. {products[i]}")
# 출력:
# 1. 노트북
# 2. 마우스
# 3. 키보드
# 4. 모니터


# ============================================
# 8. 실전 예제 3: FastAPI 리스트 데이터 처리
# ============================================

# FastAPI에서 사용자 목록을 처리할 때 이런 패턴을 사용합니다
users = ["alice@example.com", "bob@example.com", "charlie@example.com"]

print("\n=== 사용자 이메일 처리 ===")
for email in users:
    # 이메일 앞부분만 추출 (@ 앞)
    username = email.split("@")[0]
    print(f"사용자: {username}")
# 출력:
# 사용자: alice
# 사용자: bob
# 사용자: charlie
