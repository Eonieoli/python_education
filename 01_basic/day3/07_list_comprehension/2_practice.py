# 1~10의 제곱 리스트를 만드세요
# TODO: 리스트 컴프리헨션으로 squares 변수를 작성하세요
# 힌트: [x**2 for x in range(1, 11)]
squares = [x**2 for x in range(1, 11)]
print("제곱:", squares)
# 출력: 제곱: [1, 4, 9, 16, 25, 36, 49, 64, 81, 100]


# ============================================
# 실습 3: 문자열 소문자 변환 (TODO)
# ============================================

# 대문자로 된 과일 이름들
fruits = ["APPLE", "BANANA", "CHERRY", "DATE"]

# TODO: 모든 과일 이름을 소문자로 변환하세요
# 힌트: [fruit.lower() for fruit in fruits]
lower_fruits = [fruit.lower() for fruit in fruits]
print("소문자:", lower_fruits)
# 출력: 소문자: ['apple', 'banana', 'cherry', 'date']


# ============================================
# 실습 4: 필터링 + 변환 동시 적용 (TODO)
# ============================================

# 점수 리스트
scores = [45, 80, 92, 33, 67, 88, 75]

# TODO: 60점 이상인 점수만 추출하고, 각각에 +5 보너스를 추가하세요
# 힌트: [score + 5 for score in scores if score >= 60]
