# ============================================
# 1. Hello World! - 첫 번째 Python 프로그램
# ============================================

# print() 함수는 화면에 글자를 출력합니다
print("Hello, World!")  # 출력: Hello, World!

# 한글도 출력할 수 있습니다
print("안녕하세요, Python!")  # 출력: 안녕하세요, Python!


# ============================================
# 2. 주석이란?
# ============================================

# 이것은 주석입니다
# 주석은 코드 설명을 위한 메모입니다
# Python은 주석을 실행하지 않습니다

print("주석 테스트")  # 이 줄은 실행됩니다
# print("이 줄은 실행되지 않습니다")  # 앞에 #이 있으면 주석


# ============================================
# 3. 여러 줄 주석
# ============================================

"""
이것은 여러 줄 주석입니다.
큰따옴표 3개(\""")로 감싸면
여러 줄을 한번에 주석으로 만들 수 있습니다.

프로그램 설명이나
긴 메모를 남길 때 유용합니다.
"""

print("여러 줄 주석 테스트 완료!")


# ============================================
# 4. 간단한 계산 테스트
# ============================================

# Python으로 계산도 할 수 있습니다
print(10 + 5)  # 출력: 15
print(20 - 8)  # 출력: 12
print(3 * 4)  # 출력: 12
print(15 / 3)  # 출력: 5.0


# ============================================
# 축하합니다! 첫 Python 프로그램 완성!
# ============================================

print("환경 구축 완료! Python 시작 준비 끝!")
