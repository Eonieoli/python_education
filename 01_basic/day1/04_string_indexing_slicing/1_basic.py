# 1_basic.py

"""
Day 1 - 4교시: 문자열 인덱싱/슬라이싱
Basic (강사 시연용)

학습 목표:
- 인덱스로 문자열의 특정 위치 문자 접근
- 음수 인덱스 이해 (뒤에서부터 접근)
- 슬라이싱으로 문자열 일부 추출
- 다양한 슬라이싱 패턴 활용
"""

# ============================================
# 1. 인덱싱: 특정 위치의 문자 접근
# ============================================

# 문자열의 각 문자는 0부터 시작하는 인덱스를 가집니다
text = "Python"
print("문자열:", text)

# 첫 번째 문자 (인덱스 0)
print(text[0])  # 출력: P

# 두 번째 문자 (인덱스 1)
print(text[1])  # 출력: y

# 마지막 문자 (인덱스 5)
print(text[5])  # 출력: n


# ============================================
# 2. 음수 인덱스: 뒤에서부터 접근
# ============================================

# 음수 인덱스는 뒤에서부터 셉니다 (-1이 마지막)
text = "Python"

# 마지막 문자
print(text[-1])  # 출력: n

# 뒤에서 두 번째 문자
print(text[-2])  # 출력: o

# 뒤에서 세 번째 문자
print(text[-3])  # 출력: h


# ============================================
# 3. 슬라이싱: 문자열의 일부 추출
# ============================================

# 슬라이싱은 [시작:끝] 형태로 사용합니다
# 시작 인덱스는 포함되고, 끝 인덱스는 포함되지 않습니다
text = "Hello World"

# 인덱스 0부터 4까지 (5 미포함)
print(text[0:5])  # 출력: Hello

# 인덱스 6부터 10까지 (11 미포함)
print(text[6:11])  # 출력: World


# ============================================
# 4. 슬라이싱 응용 패턴
# ============================================

text = "Python Programming"

# 처음부터 6까지 (시작 생략)
print(text[:6])  # 출력: Python

# 7부터 끝까지 (끝 생략)
print(text[7:])  # 출력: Programming

# 전체 복사
print(text[:])  # 출력: Python Programming


# ============================================
# 5. step을 활용한 슬라이싱
# ============================================

text = "0123456789"

# 2칸씩 건너뛰기 (0, 2, 4, 6, 8)
print(text[::2])  # 출력: 02468

# 3칸씩 건너뛰기 (0, 3, 6, 9)
print(text[::3])  # 출력: 0369


# ============================================
# 6. 역순 문자열 (매우 유용!)
# ============================================

text = "Python"

# -1 step으로 역순 출력
print(text[::-1])  # 출력: nohtyP

# 역순 확인 예제
word = "level"
reversed_word = word[::-1]
print(word, "→", reversed_word)  # 출력: level → level


# ============================================
# 7. 실전 예제: 파일 경로 처리
# ============================================

# 파일 경로에서 파일명 추출
file_path = "C:/Documents/report.pdf"

# 마지막 / 위치 찾기 (11)
# 12부터 끝까지 추출
file_name = file_path[13:]
print("파일명:", file_name)  # 출력: 파일명: report.pdf

# 확장자 추출 (마지막 4글자)
extension = file_name[-4:]
print("확장자:", extension)  # 출력: 확장자: .pdf
