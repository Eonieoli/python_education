# ============================================
# Exercise 1: 성적표 출력하기 (정답)
# ============================================

# 주어진 데이터
student_name = "이지훈"
korean = 88
english = 92
math = 85
science = 90

# 총점 계산
total_score = korean + english + math + science  # 355

# 평균 계산
average_score = total_score / 4  # 88.75

# 성적표 출력
print("=" * 40)
print("          학생 성적표")
print("=" * 40)
print(f"이름: {student_name}")
print("-" * 40)
print(f"국어: {korean}점")
print(f"영어: {english}점")
print(f"수학: {math}점")
print(f"과학: {science}점")
print("-" * 40)
print(f"총점: {total_score}점")
print(f"평균: {average_score:.2f}점")  # 소수점 2자리
print("=" * 40)

# 출력 예시:
# ========================================
#           학생 성적표
# ========================================
# 이름: 이지훈
# ----------------------------------------
# 국어: 88점
# 영어: 92점
# 수학: 85점
# 과학: 90점
# ----------------------------------------
# 총점: 355점
# 평균: 88.75점
# ========================================


# ============================================
# Exercise 2: 카페 영수증 출력하기 (정답)
# ============================================

# 주어진 데이터
menu1 = "아메리카노"
price1 = 4500
quantity1 = 2

menu2 = "카페라떼"
price2 = 5000
quantity2 = 1

tax_rate = 0.1  # 부가세 10%

# 각 메뉴의 소계
subtotal1 = price1 * quantity1  # 9000
subtotal2 = price2 * quantity2  # 5000

# 합계
total = subtotal1 + subtotal2  # 14000

# 부가세
tax = total * tax_rate  # 1400

# 최종 금액
final_amount = total + tax  # 15400

# 영수증 출력
print("=" * 40)
print("          카페 영수증")
print("=" * 40)
print(f"{menu1:10s} {price1:>8,}원 x {quantity1}잔 = {subtotal1:>10,}원")
print(f"{menu2:10s} {price2:>8,}원 x {quantity2}잔 = {subtotal2:>10,}원")
print("-" * 40)
print(f"{'합계':20s} {total:>10,}원")
print(f"{'부가세 (10%)':20s} {tax:>10,.0f}원")
print("=" * 40)
print(f"{'최종 금액':20s} {final_amount:>10,.0f}원")
print("=" * 40)

# 출력 예시:
# ========================================
#           카페 영수증
# ========================================
# 아메리카노      4,500원 x 2잔 =      9,000원
# 카페라떼        5,000원 x 1잔 =      5,000원
# ----------------------------------------
# 합계                              14,000원
# 부가세 (10%)                       1,400원
# ========================================
# 최종 금액                         15,400원
# ========================================
