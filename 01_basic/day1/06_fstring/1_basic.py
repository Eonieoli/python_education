"""
Day 1 - 6교시: f-string
Basic (강사 시연용)

학습 목표:
- f-string 기본 문법 이해하기
- 변수를 문자열에 삽입하는 방법
- 표현식을 문자열에 포함하기
- 소수점 포매팅 방법
"""

# ============================================
# 1. f-string 기본 문법
# ============================================

# 변수를 문자열에 삽입할 때 f-string 사용
name = "홍길동"
age = 25

# f-string: f"..." 형식으로 작성, {변수}로 삽입
message = f"안녕하세요, {name}님!"
print(message)  # 출력: 안녕하세요, 홍길동님!

# 여러 변수를 함께 사용
print(f"이름: {name}, 나이: {age}세")  # 출력: 이름: 홍길동, 나이: 25세

# 기존 방식과 비교 (참고용)
print("이름:", name, ", 나이:", age, "세")  # 번거로움
print("이름: " + name + ", 나이: " + str(age) + "세")  # 복잡함


# ============================================
# 2. 표현식 포함하기
# ============================================

# f-string 안에서 계산식도 사용 가능
x = 10
y = 20
print(f"{x} + {y} = {x + y}")  # 출력: 10 + 20 = 30

# 문자열 메서드도 직접 사용 가능
name = "alice"
print(f"대문자: {name.upper()}")  # 출력: 대문자: ALICE

# 복잡한 표현식
price = 15000
quantity = 3
print(f"총 금액: {price * quantity}원")  # 출력: 총 금액: 45000원


# ============================================
# 3. 변수와 텍스트 혼합
# ============================================

# 실전 예제: 상품 정보 출력
product = "노트북"
price = 1200000
stock = 5

print(f"상품명: {product}")
print(f"가격: {price:,}원")  # 출력: 가격: 1,200,000원 (천단위 콤마)
print(f"재고: {stock}개")
print(f"{product}의 총 재고 가치: {price * stock:,}원")
# 출력: 노트북의 총 재고 가치: 6,000,000원


# ============================================
# 4. 소수점 포매팅
# ============================================

# 소수점 자리수 지정: {변수:.2f} → 소수점 2자리
pi = 3.141592653589793
print(f"원주율: {pi:.2f}")  # 출력: 원주율: 3.14
print(f"원주율: {pi:.4f}")  # 출력: 원주율: 3.1416

# 실전 예제: 평균 계산
korean = 85
english = 92
math = 88
average = (korean + english + math) / 3

print(f"국어: {korean}점")
print(f"영어: {english}점")
print(f"수학: {math}점")
print(f"평균: {average:.2f}점")  # 출력: 평균: 88.33점


# ============================================
# 5. 실전 종합 예제: 쇼핑몰 영수증
# ============================================

# 상품 정보
item1 = "티셔츠"
price1 = 29000
qty1 = 2

item2 = "청바지"
price2 = 59000
qty2 = 1

# 소계 계산
subtotal1 = price1 * qty1
subtotal2 = price2 * qty2
total = subtotal1 + subtotal2

# 할인 적용 (10%)
discount_rate = 0.1
discount_amount = total * discount_rate
final_price = total - discount_amount

# 영수증 출력
print("=" * 40)
print("       쇼핑몰 영수증")
print("=" * 40)
print(f"{item1:10s} {price1:>8,}원 x {qty1}개 = {subtotal1:>10,}원")
print(f"{item2:10s} {price2:>8,}원 x {qty2}개 = {subtotal2:>10,}원")
print("-" * 40)
print(f"{'소계':20s} {total:>10,}원")
print(f"{'할인 (10%)':20s} -{discount_amount:>9,.0f}원")
print("=" * 40)
print(f"{'최종 금액':20s} {final_price:>10,.0f}원")
print("=" * 40)

# 출력 예시:
# ========================================
#        쇼핑몰 영수증
# ========================================
# 티셔츠        29,000원 x 2개 =     58,000원
# 청바지        59,000원 x 1개 =     59,000원
# ----------------------------------------
# 소계                          117,000원
# 할인 (10%)                    -11,700원
# ========================================
# 최종 금액                     105,300원
# ========================================
