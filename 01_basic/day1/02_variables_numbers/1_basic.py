"""
Day 1 - 2교시: 변수와 숫자형
Basic (강사 시연용)

학습 목표:
- 변수 선언과 할당
- type() 함수로 타입 확인
- int와 float 타입 이해
- 사칙 연산 및 복합 연산자
"""

# ============================================
# 1. 변수 선언과 할당
# ============================================

# 변수는 데이터를 저장하는 상자입니다
age = 25
print("나이:", age)  # 출력: 나이: 25

# 변수명은 의미있게 작성합니다
student_count = 30
print("학생 수:", student_count)  # 출력: 학생 수: 30

# 여러 변수를 동시에 선언할 수도 있습니다
name, height = "홍길동", 175
print("이름:", name, "키:", height)  # 출력: 이름: 홍길동 키: 175


# ============================================
# 2. type() 함수로 타입 확인
# ============================================

# type() 함수는 변수의 자료형을 알려줍니다
age = 25
print(type(age))  # 출력: <class 'int'>

height = 175.5
print(type(height))  # 출력: <class 'float'>

# int는 정수, float는 실수(소수점이 있는 숫자)입니다
score = 100  # 정수
temperature = 36.5  # 실수


# ============================================
# 3. 사칙 연산
# ============================================

# 기본 사칙 연산
a = 10
b = 3
print(a + b)  # 출력: 13 (덧셈)
print(a - b)  # 출력: 7 (뺄셈)
print(a * b)  # 출력: 30 (곱셈)
print(a / b)  # 출력: 3.333... (나눗셈)

# 특수 연산자
print(a // b)  # 출력: 3 (몫)
print(a % b)  # 출력: 1 (나머지)
print(a ** b)  # 출력: 1000 (거듭제곱, 10의 3제곱)


# ============================================
# 4. 복합 연산자
# ============================================

# 복합 연산자는 계산과 할당을 동시에 합니다
count = 10
print("초기값:", count)  # 출력: 초기값: 10

count += 5  # count = count + 5 와 같음
print("5 더한 후:", count)  # 출력: 5 더한 후: 15

count -= 3  # count = count - 3
print("3 뺀 후:", count)  # 출력: 3 뺀 후: 12

count *= 2  # count = count * 2
print("2 곱한 후:", count)  # 출력: 2 곱한 후: 24

count /= 4  # count = count / 4
print("4 나눈 후:", count)  # 출력: 4 나눈 후: 6.0


# ============================================
# 5. 실전 예제: 상품 가격 계산
# ============================================

# 상품 정보
price = 15000  # 상품 가격
quantity = 3  # 구매 수량
discount = 0.1  # 10% 할인

# 계산
total = price * quantity
print("정가:", total, "원")  # 출력: 정가: 45000 원

# 할인 금액 계산
discounted = total * (1 - discount)
print("할인가:", discounted, "원")  # 출력: 할인가: 40500.0 원

# 할인된 금액 계산
saved = total - discounted
print("절약한 금액:", saved, "원")  # 출력: 절약한 금액: 4500.0 원
