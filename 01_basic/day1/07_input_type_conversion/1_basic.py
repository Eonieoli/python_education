# 1_basic.py

"""
Day 1 - 7교시: 입출력 + 형변환
Basic (강사 시연용)

학습 목표:
- input() 함수로 사용자 입력 받기
- input()은 항상 문자열로 반환한다는 특징 이해
- int(), float(), str() 형변환 사용하기
- 형변환 시 주의점과 에러 처리
"""

# ============================================
# 1. input() 기본 - 항상 문자열 반환!
# ============================================

# 사용자로부터 입력받기
name = input("이름을 입력하세요: ")
print(f"안녕하세요, {name}님!")  # 출력: 안녕하세요, 홍길동님!

# input()의 특징: 항상 문자열(str)로 반환됩니다
age_str = input("나이를 입력하세요: ")
print(type(age_str))  # 출력: <class 'str'>
print(f"입력값: {age_str}")  # 출력: 입력값: 25


# ============================================
# 2. 형변환의 필요성
# ============================================

# 숫자를 입력받았지만 문자열로 저장됨
age_str = "25"
print(type(age_str))  # 출력: <class 'str'>

# 문자열끼리 덧셈하면 연결됩니다
print(age_str + "10")  # 출력: 2510 (이건 우리가 원하는 게 아님!)

# 숫자로 계산하려면 형변환 필요
age_int = int(age_str)  # 문자열 → 정수 변환
print(age_int + 10)  # 출력: 35 (이게 우리가 원하는 것!)


# ============================================
# 3. int() - 문자열을 정수로 변환
# ============================================

# 문자열 → 정수
num_str = "100"
num_int = int(num_str)
print(type(num_int))  # 출력: <class 'int'>
print(num_int + 50)  # 출력: 150

# float → 정수 (소수점 버림)
pi = 3.14
pi_int = int(pi)
print(pi_int)  # 출력: 3


# ============================================
# 4. float() - 문자열을 실수로 변환
# ============================================

# 문자열 → 실수
price_str = "15.99"
price_float = float(price_str)
print(type(price_float))  # 출력: <class 'float'>
print(price_float * 2)  # 출력: 31.98

# 정수 → 실수
count = 10
count_float = float(count)
print(count_float)  # 출력: 10.0


# ============================================
# 5. str() - 숫자를 문자열로 변환
# ============================================

# 정수 → 문자열
age = 25
age_str = str(age)
print(type(age_str))  # 출력: <class 'str'>
print("나이: " + age_str + "세")  # 출력: 나이: 25세

# f-string이 더 편리하긴 합니다
print(f"나이: {age}세")  # 출력: 나이: 25세


# ============================================
# 6. 형변환 주의점 - 변환 불가능한 경우
# ============================================

# 숫자가 아닌 문자열은 int()로 변환 불가
# wrong = int("hello")  # 에러 발생! ValueError

# 소수점 있는 문자열은 int()로 바로 변환 불가
# wrong = int("3.14")  # 에러 발생! ValueError

# 올바른 방법: float()으로 먼저 변환 후 int()
correct = int(float("3.14"))
print(correct)  # 출력: 3


# ============================================
# 7. 실전 예제: 사용자 나이 입력받아 계산
# ============================================

# 사용자로부터 나이 입력받기
print("\n=== 나이 계산 프로그램 ===")
birth_year_str = input("태어난 연도를 입력하세요 (예: 1995): ")

# 문자열을 정수로 변환
birth_year = int(birth_year_str)

# 현재 연도에서 빼서 나이 계산
current_year = 2026
age = current_year - birth_year

# 결과 출력
print(f"당신은 {age}세입니다.")
