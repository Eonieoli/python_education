# 2_practice.py

"""
Day 1 - 3교시: 문자열 기본
Practice (강사와 함께 실습)

학습 목표:
- 이름 합치기
- 인사말 만들기
- 문자열 반복으로 패턴 만들기
"""

# ============================================
# 실습 1: 이름 합치기
# ============================================

# 성과 이름을 분리해서 저장
first = "김"
last = "철수"

# 성과 이름을 합쳐서 전체 이름 만들기
full_name = first + last

# 결과 출력
print("성:", first)
print("이름:", last)
print("전체 이름:", full_name)  # 출력: 전체 이름: 김철수


# ============================================
# 실습 2: 인사말 만들기
# ============================================

# 이름 입력
name = "홍길동"

# 여러 가지 인사말 만들기
greeting1 = "안녕하세요, " + name + "님!"
greeting2 = name + "님, 환영합니다!"
greeting3 = "반갑습니다, " + name + "님~"

# 결과 출력
print(greeting1)  # 출력: 안녕하세요, 홍길동님!
print(greeting2)  # 출력: 홍길동님, 환영합니다!
print(greeting3)  # 출력: 반갑습니다, 홍길동님~


# ============================================
# 실습 3: 문자열 반복으로 패턴 만들기
# ============================================

# 별표로 패턴 만들기
star = "*"
stars_5 = star * 5
stars_10 = star * 10

print(stars_5)  # 출력: *****
print(stars_10)  # 출력: **********

# 하트로 패턴 만들기
heart = "♥"
hearts = heart * 7
print(hearts)  # 출력: ♥♥♥♥♥♥♥

# 구분선 만들기
line1 = "=" * 20
line2 = "-" * 20
line3 = "~" * 20

print(line1)  # 출력: ====================
print(line2)  # 출력: --------------------
print(line3)  # 출력: ~~~~~~~~~~~~~~~~~~~~


# ============================================
# 실습 4: 제목과 구분선으로 출력 꾸미기
# ============================================

# 제목 정보
title = "Python 학습"

# 구분선 (제목 길이에 맞춰서)
separator = "=" * len(title)

# 예쁘게 출력
print(separator)  # 출력: ============
print(title)  # 출력: Python 학습
print(separator)  # 출력: ============


# ============================================
# 실습 5: 문자열 길이 활용하기
# ============================================

# 여러 문자열의 길이 비교
str1 = "Hello"
str2 = "안녕하세요"
str3 = "Good Morning"

# 각 문자열의 길이 출력
print(f"{str1} → 길이: {len(str1)}글자")  # 출력: Hello → 길이: 5글자
print(f"{str2} → 길이: {len(str2)}글자")  # 출력: 안녕하세요 → 길이: 5글자
print(f"{str3} → 길이: {len(str3)}글자")  # 출력: Good Morning → 길이: 12글자
